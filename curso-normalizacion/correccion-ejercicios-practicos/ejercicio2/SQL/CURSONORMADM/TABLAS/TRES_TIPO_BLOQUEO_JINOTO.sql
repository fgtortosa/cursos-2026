CREATE TABLE "CURSONORMADM"."TRES_TIPO_BLOQUEO_JINOTO" 
   (	"IDENTIFICADOR" NUMBER GENERATED BY DEFAULT AS IDENTITY MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER  NOCYCLE  NOKEEP  NOSCALE  NOT NULL ENABLE, 
	"CODIGO" VARCHAR2(50) COLLATE "USING_NLS_COMP" NOT NULL ENABLE, 
	"NOMBRE_ES" VARCHAR2(255) COLLATE "USING_NLS_COMP" NOT NULL ENABLE, 
	"NOMBRE_CA" VARCHAR2(255) COLLATE "USING_NLS_COMP" NOT NULL ENABLE, 
	"NOMBRE_EN" VARCHAR2(255) COLLATE "USING_NLS_COMP" NOT NULL ENABLE, 
	"COLOR" CHAR(7) COLLATE "USING_NLS_COMP", 
	"ORDEN" NUMBER, 
	"ACTIVO" CHAR(1) COLLATE "USING_NLS_COMP" DEFAULT 'S' NOT NULL ENABLE, 
	 CONSTRAINT "CHK_COLOR_HEX" CHECK (color IS NULL OR REGEXP_LIKE(color, '^#[0-9A-Fa-f]{6}$')) ENABLE, 
	 CONSTRAINT "CHK_ORDEN_RANGO" CHECK (orden IS NULL OR (orden BETWEEN 1 AND 999)) ENABLE, 
	 CONSTRAINT "CHK_ACTIVO_VALOR" CHECK (activo IN ('S','N')) ENABLE, 
	 PRIMARY KEY ("IDENTIFICADOR")
  USING INDEX  ENABLE, 
	 UNIQUE ("CODIGO")
  USING INDEX  ENABLE
   )  DEFAULT COLLATION "USING_NLS_COMP" ;